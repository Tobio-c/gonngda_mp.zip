def NutMatrix(Mjd_TT):
    """
    计算章动矩阵（从平赤道平春分点坐标系到真赤道真春分点坐标系）

    参数:
    Mjd_TT: 以TT（地球时）表示的修正儒略日

    返回:
    NutMat: 3x3章动矩阵
    """
    # 计算黄赤交角
    ep = MeanObliquity(Mjd_TT)

    # 计算黄经章动和交角章动
    dpsi, deps = NutAngles(Mjd_TT)

    # 计算章动矩阵: 真赤道真春分点 = 章动矩阵 × 平赤道平春分点
    # 转换关系: NutMat = R_x(-ep-deps) * R_z(-dpsi) * R_x(ep)
    NutMat = R_x(-(ep + deps)) @ R_z(-dpsi) @ R_x(ep)

    return NutMat


def PrecMatrix(Mjd_1, Mjd_2):
    """
    计算IAU 1976岁差模型的岁差旋转矩阵（从Mjd_1时刻坐标系到Mjd_2时刻坐标系）

    参数:
    Mjd_1: 初始时刻的修正儒略日（MJD）
    Mjd_2: 目标时刻的修正儒略日（MJD）
    const: 天文常数对象（需包含MJD_J2000和Arcs常数，对应SAT_Const类）

    返回:
    PrecMat: 3x3岁差旋转矩阵（将Mjd_1时刻的向量转换到Mjd_2时刻）
    """
    # 计算时间间隔（单位：儒略世纪，1儒略世纪=36525天）
    T = (Mjd_1 - const.MJD_J2000) / 36525.0  # 从J2000.0到Mjd_1的儒略世纪数
    dT = (Mjd_2 - Mjd_1) / 36525.0  # Mjd_1到Mjd_2的儒略世纪数

    # -------------------------- 计算岁差角（单位：弧度）--------------------------
    # zeta角：岁差的赤经岁差角
    zeta = (
                   # (2306.2181 + (1.39656 - 0.000139 * T) * T) +  # 主项
                   # ((0.30188 - 0.000344 * T) + 0.017998 * dT) * dT  # 高阶项
                   (2306.2181 + (1.39656 - 0.000139 * T) * T) +
           ((0.30188 - 0.000344 * T) + 0.017998 * dT) * dT
           ) * dT / const.Arcs  # 转换为弧度（const.Arcs=每弧度对应的角秒数）

    # z角：岁差的赤经岁差补角
    z = zeta + (
            (0.79280 + 0.000411 * T) + 0.000205 * dT  # 高阶修正项
    ) * dT * dT / const.Arcs  # 转换为弧度

    # theta角：岁差的赤纬岁差角
    theta = (
                    (2004.3109 - (0.85330 + 0.000217 * T) * T) -  # 主项
                    ((0.42665 + 0.000217 * T) + 0.041833 * dT) * dT  # 高阶项
            ) * dT / const.Arcs  # 转换为弧度

    # -------------------------- 计算岁差旋转矩阵 --------------------------
    # 岁差矩阵 = R_z(-z) * R_y(theta) * R_z(-zeta)
    # （R_z(α)：绕z轴逆时针旋转α的矩阵；R_y(β)：绕y轴逆时针旋转β的矩阵）
    PrecMat = R_z(-z) @ R_y(theta) @ R_z(-zeta)

    return PrecMat


def timediff(UT1_UTC, TAI_UTC):
    """
    计算不同时间系统之间的差异（单位：秒）

    参数:
    UT1_UTC: UT1与UTC的时差（单位：秒），可从IERS函数获取
    TAI_UTC: TAI与UTC的时差（单位：秒），可从IERS函数获取

    返回:
    UT1_TAI: UT1与TAI的时差（秒）
    UTC_GPS: UTC与GPS的时差（秒）
    UT1_GPS: UT1与GPS的时差（秒）
    TT_UTC: 地球时（TT）与UTC的时差（秒）
    GPS_UTC: GPS时间与UTC的时差（秒）
    """
    # 定义固定的时间系统差（国际天文学联合会IAU规定的常数）
    TT_TAI = +32.184  # TT与TAI的时差（固定值，秒）
    GPS_TAI = -19.0  # GPS与TAI的时差（固定值，秒）

    # 计算中间时间差
    TT_GPS = TT_TAI - GPS_TAI  # TT与GPS的时差（秒）
    TAI_GPS = -GPS_TAI  # TAI与GPS的时差（秒）

    # 计算输出的时间系统差
    UT1_TAI = UT1_UTC - TAI_UTC  # UT1与TAI的时差
    UTC_TAI = -TAI_UTC  # UTC与TAI的时差
    UTC_GPS = UTC_TAI - GPS_TAI  # UTC与GPS的时差
    UT1_GPS = UT1_TAI - GPS_TAI  # UT1与GPS的时差
    TT_UTC = TT_TAI - UTC_TAI  # TT与UTC的时差
    GPS_UTC = GPS_TAI - UTC_TAI  # GPS与UTC的时差

    return UT1_TAI, UTC_GPS, UT1_GPS, TT_UTC, GPS_UTC


def frac(x):
    """
    计算一个数的小数部分（与MATLAB的Frac函数功能一致）

    参数:
    x: 输入数值

    返回:
    数值的小数部分（范围[0, 1)）
    """
    return x - np.floor(x)


def gmst(Mjd_UT1):
    """
    计算格林威治平恒星时（Greenwich Mean Sidereal Time, GMST）

    参数:
    Mjd_UT1: 以UT1时间表示的修正儒略日（MJD）

    返回:
    gmstime: 格林威治平恒星时（单位：弧度，范围[0, 2π)）
    """
    # 常数定义
    Secs = 86400.0  # 一天的秒数
    MJD_J2000 = 51544.5  # J2000.0历元的修正儒略日

    # 提取MJD的整数部分（对应UT1的0时）
    Mjd_0 = np.floor(Mjd_UT1)

    # 计算UT1时刻在当天的秒数（0~86400秒）
    UT1 = Secs * (Mjd_UT1 - Mjd_0)

    # 计算从J2000.0历元起算的儒略世纪数
    T_0 = (Mjd_0 - MJD_J2000) / 36525.0  # 对应Mjd_0的儒略世纪数
    T = (Mjd_UT1 - MJD_J2000) / 36525.0  # 对应Mjd_UT1的儒略世纪数

    # 计算格林威治平恒星时（单位：秒）
    # 公式包含：常数项 + 长期项（基于T_0） + 每日变化项（基于UT1） + 高阶修正项
    gmst_sec = (24110.54841
                + 8640184.812866 * T_0
                + 1.002737909350795 * UT1
                + (0.093104 - 6.2e-6 * T) * T * T)

    # 计算小数部分（取模确保在0~86400秒范围内），转换为弧度（0~2π）
    gmstime = 2 * np.pi * frac(gmst_sec / Secs)

    return gmstime


def EqnEquinox(Mjd_TT):
    dpsi, deps = NutAngles(Mjd_TT)
    return dpsi * np.cos(MeanObliquity(Mjd_TT))


def gast(Mjd_UT1):
    gstime = np.mod(gmst(Mjd_UT1) + EqnEquinox(Mjd_UT1), const.pi2)

    return gstime


def GHAMatrix(Mjd_UT1):
    return R_z(gast(Mjd_UT1))


def PoleMatrix(xp, yp):
    return R_y(-xp) @ R_x(-yp)


def ECEF2ECI(MJD_UTC, Y0):
    """
    将地球固定坐标系（ECEF）坐标转换为地球惯性坐标系（ECI）坐标

    参数:
    MJD_UTC: 修正儒略日(UTC)
    Y0: ECEF坐标系下的位置和速度向量 [x, y, z, vx, vy, vz]
    eopdata: 地球定向参数数据
    const: 包含天文常数的对象

    返回:
    Y: ECI坐标系下的位置和速度向量 [x, y, z, vx, vy, vz]
    """
    # 获取地球定向参数
    x_pole, y_pole, UT1_UTC, LOD, dpsi, deps, dx_pole, dy_pole, TAI_UTC = IERS(MJD_UTC, 'l')

    # 计算时间差参数
    UT1_TAI, UTC_GPS, UT1_GPS, TT_UTC, GPS_UTC = timediff(UT1_UTC, TAI_UTC)

    # 计算不同时间系统的MJD
    MJD_UT1 = MJD_UTC + UT1_UTC / 86400.0
    MJD_TT = MJD_UTC + TT_UTC / 86400.0

    # 计算各种旋转矩阵
    P = PrecMatrix(const.MJD_J2000, MJD_TT)  # IAU 1976岁差矩阵
    N = NutMatrix(MJD_TT)  # IAU 1980章动矩阵
    Theta = GHAMatrix(MJD_UT1)  # 地球自转矩阵
    Pi = PoleMatrix(x_pole, y_pole)  # 极移矩阵

    # 构造S矩阵 (地球自转的导数)
    S = np.zeros((3, 3))
    S[0, 1] = 1
    S[1, 0] = -1

    # 计算地球自转角速度 [rad/s]
    Omega = 7292115.8553e-11 + 4.3e-15 * ((MJD_UTC - const.MJD_J2000) / 36525)
    # 另一种计算方式：Omega = const.omega_Earth - 0.843994809e-9 * LOD  # IERS推荐

    # 计算地球自转矩阵的导数
    dTheta = Omega * S @ Theta  # 矩阵乘法

    # 计算ICRS到ITRS的转换矩阵及其导数
    U = Pi @ Theta @ N @ P  # ICRS到ITRS的转换矩阵
    dU = Pi @ dTheta @ N @ P  # 转换矩阵的导数 [1/s]

    # 从WGS到ICRS的转换
    r_ecef = np.array(Y0[:3]).reshape(3, 1)  # ECEF位置向量
    v_ecef = np.array(Y0[3:]).reshape(3, 1)  # ECEF速度向量

    # 转换到ECI坐标系
    r_eci = U.T @ r_ecef  # 位置转换
    v_eci = U.T @ v_ecef + dU.T @ r_ecef  # 速度转换

    # 组合结果
    Y = np.vstack((r_eci, v_eci)).flatten()

    return Y
